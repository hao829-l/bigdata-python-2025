#   算术运算符

a = 18
print(a % 4)

print(2 ** 2)
print(2 ** 4)
print(8**0.5)
print(8**(1/2))

print(8**(1/3))
print(pow(8,1/3))       # pow计算幂运算的函数
print(pow(111,3))
print(pow(2,3,5))       # 先计算2**3再取余（2**3）%5


